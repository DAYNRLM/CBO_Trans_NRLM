package com.nrlm.cbo.model.settingsModels;

public class NomineeModel {

    String position;
    String relationName;
    String relationCode;
    String nomineeName;
    String shgActivityMember;
    String shgActivityindividual;

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public String getRelationName() {
        return relationName;
    }

    public void setRelationName(String relationName) {
        this.relationName = relationName;
    }

    public String getRelationCode() {
        return relationCode;
    }

    public void setRelationCode(String relationCode) {
        this.relationCode = relationCode;
    }

    public String getNomineeName() {
        return nomineeName;
    }

    public void setNomineeName(String nomineeName) {
        this.nomineeName = nomineeName;
    }

    public String getShgActivityMember() {
        return shgActivityMember;
    }

    public void setShgActivityMember(String shgActivityMember) {
        this.shgActivityMember = shgActivityMember;
    }

    public String getShgActivityindividual() {
        return shgActivityindividual;
    }

    public void setShgActivityindividual(String shgActivityindividual) {
        this.shgActivityindividual = shgActivityindividual;
    }
}
